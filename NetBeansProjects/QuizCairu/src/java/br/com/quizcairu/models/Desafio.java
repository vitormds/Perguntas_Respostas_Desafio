package br.com.quizcairu.models;

public class Desafio {
    private Integer id;
    private Integer alunoId;
    private Integer acertosAluno;
    private Integer desafiadoId;
    private Integer acertosDesafiado;
    private Integer vencedorId;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public Integer getAlunoId() {
        return alunoId;
    }

    public void setAlunoId(Integer alunoId) {
        this.alunoId = alunoId;
    }

    public Integer getAcertosAluno() {
        return acertosAluno;
    }

    public void setAcertosAluno(Integer acertosAluno) {
        this.acertosAluno = acertosAluno;
    }

    public Integer getDesafiadoId() {
        return desafiadoId;
    }

    public void setDesafiadoId(Integer desafiadoId) {
        this.desafiadoId = desafiadoId;
    }

    public Integer getAcertosDesafiado() {
        return acertosDesafiado;
    }

    public void setAcertosDesafiado(Integer acertosDesafiado) {
        this.acertosDesafiado = acertosDesafiado;
    }

    public Integer getVencedorId() {
        return vencedorId;
    }

    public void setVencedorId(Integer vencedorId) {
        this.vencedorId = vencedorId;
    }
    
    
}
